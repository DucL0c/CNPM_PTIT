    private void jButton_NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_NextActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) { // Kiểm tra xem có dòng nào được chọn không
            Object o = jTable1.getValueAt(selectedRow, 1);
            Object b = jTable1.getValueAt(selectedRow, 2);
            int id = Integer.parseInt(o.toString());
            String name = b.toString();
            if ((evt.getSource() instanceof JButton)
                    && (((JButton) evt.getSource()).equals(jButton_Next))) {
                //push 1 list round vào selectRoundFRM
                (new HistoryFrm(receptionist, id,name,idRound)).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this,
                        "This function is under construction!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 cờ thủ!");
        }

    }//GEN-LAST:event_jButton_NextActionPerformed

