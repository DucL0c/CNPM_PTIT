    public void displayChessPlayerData(ArrayList<ChessPlayer> listPlayer) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int stt=1;
        for (ChessPlayer chessPlayer : listPlayer) {
            Object[] row = {
                stt++,
                chessPlayer.getId(),
                chessPlayer.getName(),
                chessPlayer.getDob(),
                chessPlayer.getNational(),
                chessPlayer.getElo(),
            };
            model.addRow(row);
        }
    }

